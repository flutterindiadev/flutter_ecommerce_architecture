class Address {
  final int id;
  final String streetName;
  final String country;
  final String city;
  final int pincode;
  final String floor;
  final String building;
  final String apartment;

  Address({
    required this.id,
    required this.streetName,
    required this.country,
    required this.city,
    required this.pincode,
    required this.floor,
    required this.building,
    required this.apartment,
  });
}
